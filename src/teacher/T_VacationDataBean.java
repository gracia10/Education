package teacher;

public class T_VacationDataBean {

	private int vc_num;
	private String t_id;
	private String vc_start;
	private String vc_end;
	private String reason;
	
	public int getVc_num() {
		return vc_num;
	}
	public String getT_id() {
		return t_id;
	}
	public String getVc_start() {
		return vc_start;
	}
	public String getVc_end() {
		return vc_end;
	}
	public String getReason() {
		return reason;
	}
	public void setVc_num(int vc_num) {
		this.vc_num = vc_num;
	}
	public void setT_id(String t_id) {
		this.t_id = t_id;
	}
	public void setVc_start(String vc_start) {
		this.vc_start = vc_start;
	}
	public void setVc_end(String vc_end) {
		this.vc_end = vc_end;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	
	
	
}